package com.lynn.pojo;

public class AssistantFeedback {
    int feedback_id;
    String feedback_resume;
    String feedback_detail;
    String feedback_modular;
    String feedback_frequency;
    int feedback_date;
    String feedback_user;
    int feedback_state;

    public int getFeedback_id() {
        return feedback_id;
    }

    public void setFeedback_id(int feedback_id) {
        this.feedback_id = feedback_id;
    }

    public String getFeedback_resume() {
        return feedback_resume;
    }

    public void setFeedback_resume(String feedback_resume) {
        this.feedback_resume = feedback_resume;
    }

    public String getFeedback_detail() {
        return feedback_detail;
    }

    public void setFeedback_detail(String feedback_detail) {
        this.feedback_detail = feedback_detail;
    }

    public String getFeedback_modular() {
        return feedback_modular;
    }

    public void setFeedback_modular(String feedback_modular) {
        this.feedback_modular = feedback_modular;
    }

    public String getFeedback_frequency() {
        return feedback_frequency;
    }

    public void setFeedback_frequency(String feedback_frequency) {
        this.feedback_frequency = feedback_frequency;
    }

    public int getFeedback_date() {
        return feedback_date;
    }

    public void setFeedback_date(int feedback_date) {
        this.feedback_date = feedback_date;
    }

    public String getFeedback_user() {
        return feedback_user;
    }

    public void setFeedback_user(String feedback_user) {
        this.feedback_user = feedback_user;
    }

    public int getFeedback_state() {
        return feedback_state;
    }

    public void setFeedback_state(int feedback_state) {
        this.feedback_state = feedback_state;
    }
}
